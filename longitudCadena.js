//Esta es la definición de la función obtenerLongitud
//Toma un parámetro 'cadena', que debe ser una cadena de texto

function obtenerLongitud(cadena){
    return cadena.length; //Devuelve la longitud de la cadena con el método 'length'
}

//Ejemplos de uso de la función
//Llamamos a la función con distintos argumentos para obtener la longitud de las cadenas 

console.log(obtenerLongitud("Hola mundo")); //devuelve 10
console.log(obtenerLongitud("Ingenieria en sistemas")); //devuelve 22
console.log(obtenerLongitud("")); //devuelve 0
